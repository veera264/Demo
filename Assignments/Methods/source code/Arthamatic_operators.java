package Task6;

public class Arthamatic_operators {
	
	static void add(int i,int j) {
		int k=i+j;
		System.out.println("Addition of two numbers: "+k);
	}
	static void sub(int i,int j) {
		int k=i-j;
		System.out.println("Addition of two numbers: "+k);
	}
	static void mul(int i,int j) {
		System.out.println("Addition of two numbers: "+i*j);
	}
	static void div(int i,int j) {
		System.out.println("Addition of two numbers: "+i/j);
	}
	public static void main(String[] args) {
		
		add(2,3);
		sub(2,3);
		mul(2,3);
		div(2,3);

	}

}
